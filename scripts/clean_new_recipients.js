async function cleanRecipients() {
    console.log('🧹 Cleaning recipient list...');
    
    // 1. Split into array and trim
    let lines = rawEmails.split('\n');
    let emails = [];
    for (let line of lines) {
        let trimmed = line.trim();
        if (trimmed.includes('@')) {
            // Fix double dots
            let fixed = trimmed.replace(/\.\./g, '.');
            emails.push(fixed);
        }
    }
    
    // 2. Remove duplicates
    emails = [...new Set(emails)];
    
    console.log(`✅ Cleaned list contains ${emails.length} unique recipients.`);
    
    // 3. Save to recipients.txt
    const recipientsPath = path.join(__dirname, 'recipients.txt');
    
    // Backup current recipients.txt if it exists
    if (fs.existsSync(recipientsPath)) {
        const backupPath = recipientsPath + '.bak';
        fs.copyFileSync(recipientsPath, backupPath);
        console.log(`📦 Backed up existing recipients.txt to ${backupPath}`);
    }
    
    fs.writeFileSync(recipientsPath, emails.join('\n'));
    console.log(`📄 Saved cleaned list to: ${recipientsPath}`);
}

